#ifndef LIBTYPES_H
#define LIBTYPES_H

static const unsigned short BYTES_KB = 1024;
static const unsigned long BYTES_MB = (1024ULL * BYTES_KB);
static const unsigned long long BYTES_GB = (1024ULL * BYTES_MB);

typedef unsigned long long Byte;
typedef unsigned long Kilobyte;
typedef unsigned long Megabyte;
typedef unsigned short Gigabyte;

#endif
